/** Em JS temos os operadores arítmeticos que são 
 * +,-,/,*,%
 */

 /**Como facilitador pode-se ter resultados de algumas
  * operações com o objeto Math
  */

// Potência
  console.log(Math.pow(2,53))

// Arredondamento para o inteiro mais próximo
console.log(Math.round(.6))

// Arredondamento para cima para o inteiro
console.log(Math.ceil(.6))

// Arredondamento para baixo para o inteiro
console.log(Math.floor(.6))

// Valor absoluto
console.log(Math.abs(-5))

// Retorna o maior argumento
let x = 10
let y = 5
let z = 15

console.log(Math.max(x, y, z))

// Retorna o menor argumento
console.log(Math.min(x, y, z))

// Número pseudoaleatório x, onde x está entre 0 e 1.0
console.log(Math.random())

// Circunferência de um círculo
console.log(Math.PI)

// Número de Euler ou a base do logaritmo natural
console.log(Math.E)

// Raiz quadrada
console.log(Math.sqrt(3))

// Raiz de forma a escolher o índice
console.log(Math.pow(3, 1/3))
