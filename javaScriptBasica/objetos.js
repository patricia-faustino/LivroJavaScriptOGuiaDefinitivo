// Tipo mais importante de JavaScript é um objeto
// coleção de pares nome/ valor ou uma string para mapa de valores

var book = {
    topic: "JavaScript",
    long: true
}

// Acesse as propriedades com . ou []
console.log(book.topic)
console.log(book.long)

// Crie novas propriedades por meio de atribuição
book.autor = "Flanagan"

// Objeto vazio sem qualquer propriedade
book.contents = {}
